package foodoo.com.adapter;

public class MenuList {

    private String id,name,contact_no,cuisines,average_cost,payment_modes,opening_hours,address,image,category_name;

    public MenuList() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContact_no() {
        return contact_no;
    }

    public void setContact_no(String contact_no) {
        this.contact_no = contact_no;
    }

    public String getCuisines() {
        return cuisines;
    }

    public void setCuisines(String cuisines) {
        this.cuisines = cuisines;
    }

    public String getAverage_cost() {
        return average_cost;
    }

    public void setAverage_cost(String average_cost) {
        this.average_cost = average_cost;
    }

    public String getPayment_modes() {
        return payment_modes;
    }

    public void setPayment_modes(String payment_modes) {
        this.payment_modes = payment_modes;
    }

    public String getOpening_hours() {
        return opening_hours;
    }

    public void setOpening_hours(String opening_hours) {
        this.opening_hours = opening_hours;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getCategory_name() {
        return category_name;
    }

    public void setCategory_name(String category_name) {
        this.category_name = category_name;
    }
}
